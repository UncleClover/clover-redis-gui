package com.clover.redis.gui.model;

public class Keys {
	private String key;
	private String type;
	private String size;

	public String getKey() {
		return key;
	}

	public String getType() {
		return type;
	}

	public String getSize() {
		return size;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public void setType(String type) {
		this.type = type;
	}

	public void setSize(String size) {
		this.size = size;
	}

}
